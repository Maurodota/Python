# Realize um codigo que possa fazer uma conversão de graus Celsius em Fahrenheit que será informado pelo usuário

celsius = int (input ('Digite um valor de graus Celsius na escala de 0 ~ 100 = '))
conversao = (celsius * 9/5) + 32 
print ('A conversão de ' + str(celsius) + 'ºC Celsius equivale a: ' + str(conversao) + 'ºF graus Fahrenheit')

