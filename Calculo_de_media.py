# Criar um código onde o usuario ira inserir as notas e realizar o calculo de media das notas inseridas. 

nota1 = int(input('Inserir a nota: ')) # Inserir o numero da variavel 
#print (type(nota1))
nota2 = int(input('inserir a nota: ')) # Inserir o numero da variavel
#print (type(nota2))
media = (nota1 + nota2) / 2 # Realiza a soma das variaveis inseridas e faz a divisão das "2" notas por 2.
#print (type(media))
print ('A nota final é: ' + str(media))
